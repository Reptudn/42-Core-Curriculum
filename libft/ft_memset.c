#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	_c;
	int				i;

	_c = c;
	i = 0;
	while (i < len)
	{
		((char *)b)[i++] = _c;
	}
	return (b);
}

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != 0)
	{
		if (s[i] == c)
			return (&((char *)s)[i]);
		i++;
	}
	if (c == '\0' && s[1] == c)
		return (&((char *)s)[i]);
	return (0);
}

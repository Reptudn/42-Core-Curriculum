#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	while(n > 0)
	{
		c = n % 10;
		write(fd, &c, 1);
		n /= 10;
	}
}
